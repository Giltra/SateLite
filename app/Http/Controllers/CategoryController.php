     */
    public function store(Request $request)
    {
      $rules = [
          'name' => 'required'
      ];

      $msg =[
          'el ::attribute es requerido'
      ];

      $this->validate($request,$rules,$msg);

      $categoria = new Category($request->all());

      $categoria->save();

      return redirect('/categories');
    }

